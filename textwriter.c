#define NUM_PAGES (sizeof(text_pages) / sizeof(text_pages[0]))

// Static state
static Star stars[NUM_STARS];
static int current_page = 0;
static int current_char = 0;
static int typing_timer = 0;
static int cursor_blink_timer = 0;
static int scrolling = 0;
static int scroll_offset = 0;
static pixel_t text_buffer[SCREEN_WIDTH * SCREEN_HEIGHT];

// Helper function to draw a character at a specific position
static void draw_char(pixel_t *pixels, char c, int x, int y, pixel_t color) {
    // Only handle printable ASCII
    if (c < 32 || c > 127) {
        return;
    }

    const uint8_t *glyph = cp437_font[c - 32];

    for (int py = 0; py < 8; py++) {
        for (int px = 0; px < 8; px++) {
            int screen_x = x + px;
            int screen_y = y + py;

            if (screen_x >= 0 && screen_x < SCREEN_WIDTH &&
                screen_y >= 0 && screen_y < SCREEN_HEIGHT) {

                uint8_t row = glyph[py];
                if (row & (1 << px)) {
                    pixels[screen_y * SCREEN_WIDTH + screen_x] = color;
                }
            }
        }
    }
}

// Helper function to draw a cursor block
static void draw_cursor(pixel_t *pixels, int x, int y, pixel_t color) {
    for (int py = 0; py < 8; py++) {
        for (int px = 0; px < 8; px++) {
            int screen_x = x + px;
            int screen_y = y + py;

            if (screen_x >= 0 && screen_x < SCREEN_WIDTH &&
                screen_y >= 0 && screen_y < SCREEN_HEIGHT) {
                pixels[screen_y * SCREEN_WIDTH + screen_x] = color;
            }
        }
    }
}

void textwriter_init(void) {
    // Initialize stars at random positions
    for (int i = 0; i < NUM_STARS; i++) {
        stars[i].x = (float)(rand() % (SCREEN_WIDTH * 2)) - SCREEN_WIDTH;
        stars[i].y = (float)(rand() % (SCREEN_HEIGHT * 2)) - SCREEN_HEIGHT;
        stars[i].z = (float)(rand() % 256) + 1;
    }

    // Reset text state
    current_page = 0;
    current_char = 0;
    typing_timer = 0;
    cursor_blink_timer = 0;
    scrolling = 0;
    scroll_offset = 0;

    // Clear text buffer
    memset(text_buffer, 0, sizeof(text_buffer));
}

void textwriter_update(pixel_t *pixels, uint32_t time) {
    // === RENDER STARFIELD BACKGROUND ===
    memset(pixels, 0, SCREEN_WIDTH * SCREEN_HEIGHT * sizeof(pixel_t));

    float speed = 2.0f;
    for (int i = 0; i < NUM_STARS; i++) {
        stars[i].z -= speed;

        if (stars[i].z <= 0) {
            stars[i].x = (float)(rand() % (SCREEN_WIDTH * 2)) - SCREEN_WIDTH;
            stars[i].y = (float)(rand() % (SCREEN_HEIGHT * 2)) - SCREEN_HEIGHT;
            stars[i].z = 256.0f;
        }

        float k = 128.0f / stars[i].z;
        int screen_x = (int)(stars[i].x * k + SCREEN_WIDTH / 2);
        int screen_y = (int)(stars[i].y * k + SCREEN_HEIGHT / 2);

        if (screen_x >= 0 && screen_x < SCREEN_WIDTH &&
            screen_y >= 0 && screen_y < SCREEN_HEIGHT) {

            int brightness = (int)(255 - stars[i].z);
            if (brightness < 0) brightness = 0;
            if (brightness > 255) brightness = 255;

            pixel_t color = RGB(brightness, brightness, brightness);
            pixels[screen_y * SCREEN_WIDTH + screen_x] = color;

            if (stars[i].z < 64) {
                if (screen_x > 0) {
                    pixels[screen_y * SCREEN_WIDTH + screen_x - 1] = color;
                }
                if (screen_x < SCREEN_WIDTH - 1) {
                    pixels[screen_y * SCREEN_WIDTH + screen_x + 1] = color;
                }
                if (screen_y > 0) {
                    pixels[(screen_y - 1) * SCREEN_WIDTH + screen_x] = color;
                }
                if (screen_y < SCREEN_HEIGHT - 1) {
                    pixels[(screen_y + 1) * SCREEN_WIDTH + screen_x] = color;
                }
            }
        }
    }

    // === HANDLE SCROLLING TRANSITION ===
    if (scrolling) {
        scroll_offset += SCROLL_SPEED;

        // Draw old text scrolling up
        for (int y = 0; y < SCREEN_HEIGHT; y++) {
            int src_y = y + scroll_offset;
            if (src_y < SCREEN_HEIGHT) {
                for (int x = 0; x < SCREEN_WIDTH; x++) {
                    pixel_t text_pixel = text_buffer[src_y * SCREEN_WIDTH + x];
                    if (text_pixel != 0) {
                        pixels[y * SCREEN_WIDTH + x] = text_pixel;
                    }
                }
            }
        }

        // When scroll is complete, switch to new page
        if (scroll_offset >= SCREEN_HEIGHT) {
            scrolling = 0;
            scroll_offset = 0;
            current_page = (current_page + 1) % NUM_PAGES;
            current_char = 0;
            typing_timer = 0;
            memset(text_buffer, 0, sizeof(text_buffer));
        }

        return;  // Skip typing during scroll
    }

    // === TEXT TYPING LOGIC ===
    const char *text = text_pages[current_page];
    int text_len = strlen(text);

    // Update typing timer
    typing_timer++;
    if (typing_timer >= TYPING_SPEED && current_char < text_len) {
        current_char++;
        typing_timer = 0;
    }

    // Check if page is complete
    if (current_char >= text_len) {
        // Wait a bit, then start scrolling
        if (typing_timer > 120) {  // Wait ~2 seconds
            scrolling = 1;
            scroll_offset = 0;
        }
    }

    // === RENDER TEXT TO BUFFER ===
    memset(text_buffer, 0, sizeof(text_buffer));

    int cursor_x = 20;  // Start position
    int cursor_y = 20;
    pixel_t text_color = RGB(0, 255, 0);  // Classic green terminal text

    for (int i = 0; i < current_char; i++) {
        char c = text[i];

        if (c == '\n') {
            cursor_y += CHAR_HEIGHT;
            cursor_x = 20;
        } else {
            draw_char(text_buffer, c, cursor_x, cursor_y, text_color);
            cursor_x += CHAR_WIDTH;

            // Wrap to next line if needed
            if (cursor_x >= SCREEN_WIDTH - 20) {
                cursor_x = 20;
                cursor_y += CHAR_HEIGHT;
            }
        }
    }

    // === DRAW BLINKING CURSOR ===
    if (current_char < text_len) {
        cursor_blink_timer++;
        if ((cursor_blink_timer / CURSOR_BLINK_SPEED) % 2 == 0) {
            pixel_t cursor_color = RGB(255, 255, 255);  // White cursor
            draw_cursor(text_buffer, cursor_x, cursor_y, cursor_color);
        }
    }

    // === COMPOSITE TEXT OVER STARFIELD ===
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) {
        if (text_buffer[i] != 0) {
            pixels[i] = text_buffer[i];
        }
    }
}

