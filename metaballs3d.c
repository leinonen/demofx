// Vector math helpers
static float vec3_dot(vec3_t a, vec3_t b) {
    return a.x * b.x + a.y * b.y + a.z * b.z;
}

static vec3_t vec3_normalize(vec3_t v) {
    float len = sqrtf(v.x * v.x + v.y * v.y + v.z * v.z);
    if (len > 0.0001f) {
        v.x /= len;
        v.y /= len;
        v.z /= len;
    }
    return v;
}

// Calculate scalar field value at a point (metaball influence)
static float calculate_field(float x, float y, float z) {
    float value = 0.0f;
    for (int i = 0; i < NUM_METABALLS; i++) {
        float dx = x - metaballs[i].pos.x;
        float dy = y - metaballs[i].pos.y;
        float dz = z - metaballs[i].pos.z;
        float dist_sq = dx*dx + dy*dy + dz*dz;
        if (dist_sq > 0.001f) {  // Avoid division by zero
            value += metaballs[i].strength / dist_sq;
        }
    }
    return value;
}

// Calculate normal at a point using gradient of scalar field
static vec3_t calculate_normal(float x, float y, float z) {
    float delta = 0.01f;
    vec3_t normal;

    normal.x = calculate_field(x + delta, y, z) - calculate_field(x - delta, y, z);
    normal.y = calculate_field(x, y + delta, z) - calculate_field(x, y - delta, z);
    normal.z = calculate_field(x, y, z + delta) - calculate_field(x, y, z - delta);

    return vec3_normalize(normal);
}

// Linear interpolation between two 3D points
static vec3_t lerp_vertex(vec3_t v1, vec3_t v2, float val1, float val2) {
    vec3_t result;
    float t = (ISO_LEVEL - val1) / (val2 - val1);
    result.x = v1.x + t * (v2.x - v1.x);
    result.y = v1.y + t * (v2.y - v1.y);
    result.z = v1.z + t * (v2.z - v1.z);
    return result;
}

// Marching cubes for a single cube
static void march_cube(int x, int y, int z) {
    if (triangle_count >= MAX_TRIANGLES - 5) return;  // Safety check

    float scale = GRID_SCALE / GRID_SIZE;
    float offset = GRID_SCALE * 0.5f;

    // Get grid values at cube corners
    float cube_vals[8];
    vec3_t world_verts[8];
    for (int i = 0; i < 8; i++) {
        int gx = x + cube_vertices[i].x;
        int gy = y + cube_vertices[i].y;
        int gz = z + cube_vertices[i].z;
        cube_vals[i] = grid[gx][gy][gz];
        // Convert grid coordinates to world coordinates
        world_verts[i].x = gx * scale - offset;
        world_verts[i].y = gy * scale - offset;
        world_verts[i].z = gz * scale - offset;
    }

    // Determine cube configuration index
    int cube_index = 0;
    for (int i = 0; i < 8; i++) {
        if (cube_vals[i] > ISO_LEVEL) {
            cube_index |= (1 << i);
        }
    }

    // Check if cube is completely inside or outside
    if (edge_table[cube_index] == 0) return;

    // Find vertices on edges that are intersected
    vec3_t vert_list[12];
    for (int i = 0; i < 12; i++) {
        if (edge_table[cube_index] & (1 << i)) {
            int v1 = cube_edges[i][0];
            int v2 = cube_edges[i][1];
            vert_list[i] = lerp_vertex(world_verts[v1], world_verts[v2],
                                       cube_vals[v1], cube_vals[v2]);
        }
    }

    // Create triangles from triangle table
    for (int i = 0; triangle_table[cube_index][i] != -1; i += 3) {
        triangle_t tri;
        tri.v[0] = vert_list[triangle_table[cube_index][i]];
        tri.v[1] = vert_list[triangle_table[cube_index][i + 1]];
        tri.v[2] = vert_list[triangle_table[cube_index][i + 2]];

        // Calculate normals at each vertex
        tri.n[0] = calculate_normal(tri.v[0].x, tri.v[0].y, tri.v[0].z);
        tri.n[1] = calculate_normal(tri.v[1].x, tri.v[1].y, tri.v[1].z);
        tri.n[2] = calculate_normal(tri.v[2].x, tri.v[2].y, tri.v[2].z);

        // Calculate average depth for sorting
        tri.depth = (tri.v[0].z + tri.v[1].z + tri.v[2].z) / 3.0f;

        triangles[triangle_count++] = tri;
    }
}

// Generate mesh from scalar field using marching cubes
static void generate_mesh(void) {
    triangle_count = 0;

    // March through all cubes in the grid
    for (int x = 0; x < GRID_MAX; x++) {
        for (int y = 0; y < GRID_MAX; y++) {
            for (int z = 0; z < GRID_MAX; z++) {
                march_cube(x, y, z);
            }
        }
    }
}

// Sample scalar field into grid
static void sample_field(void) {
    float scale = GRID_SCALE / GRID_SIZE;
    float offset = GRID_SCALE * 0.5f;

    for (int x = 0; x < GRID_SIZE; x++) {
        for (int y = 0; y < GRID_SIZE; y++) {
            for (int z = 0; z < GRID_SIZE; z++) {
                float wx = x * scale - offset;
                float wy = y * scale - offset;
                float wz = z * scale - offset;
                grid[x][y][z] = calculate_field(wx, wy, wz);
            }
        }
    }
}

// Project 3D point to 2D screen coordinates
static void project(vec3_t v, int *sx, int *sy) {
    float scale = 400.0f;  // Projection scale
    float z_offset = 15.0f;  // Camera distance

    float z = v.z + z_offset;
    if (z < 1.0f) z = 1.0f;  // Avoid division by zero

    *sx = (int)(SCREEN_WIDTH / 2 + (v.x * scale) / z);
    *sy = (int)(SCREEN_HEIGHT / 2 - (v.y * scale) / z);
}

// Comparison function for depth sorting
static int compare_triangles(const void *a, const void *b) {
    const triangle_t *ta = (const triangle_t *)a;
    const triangle_t *tb = (const triangle_t *)b;
    if (ta->depth < tb->depth) return 1;
    if (ta->depth > tb->depth) return -1;
    return 0;
}

// Fill a flat-topped triangle with color interpolation
static void fill_flat_top_triangle(pixel_t *pixels, int x1, int y1, int x2, int y2, int x3, int y3,
                                   int c1, int c2, int c3) {
    if (y3 <= y1) return;

    float inv_slope1 = (float)(x3 - x1) / (y3 - y1);
    float inv_slope2 = (float)(x3 - x2) / (y3 - y2);

    float cur_x1 = x1;
    float cur_x2 = x2;

    for (int y = y1; y <= y3; y++) {
        if (y >= 0 && y < SCREEN_HEIGHT) {
            int start_x = (int)cur_x1;
            int end_x = (int)cur_x2;

            if (start_x > end_x) {
                int tmp = start_x;
                start_x = end_x;
                end_x = tmp;
            }

            float t_y = (float)(y - y1) / (y3 - y1);
            int color = c1 + (int)(t_y * (c3 - c1));

            for (int x = start_x; x <= end_x; x++) {
                if (x >= 0 && x < SCREEN_WIDTH) {
                    pixels[y * SCREEN_WIDTH + x] = palette[color & 0xFF];
                }
            }
        }

        cur_x1 += inv_slope1;
        cur_x2 += inv_slope2;
    }
}

