// Code-like text snippets to display
static const char* code_snippets[] = {
    "init()", "exec()", "free()", "run()", "malloc()",
    "0xFF", "0x42", "0xDEAD", "0xBEEF", "0x00",
    "x=0", "i++", "n--", "val", "ptr->",
    "if", "for", "int", "void", "ret",
    "&&", "||", "==", "!=", "+=",
    "{", "}", "[", "]", "(", ")",
    "01010", "1101", "0011", "1001",
    "NULL", "true", "this", "self",
    "len", "buf", "tmp", "err",
};
static const int num_snippets = sizeof(code_snippets) / sizeof(code_snippets[0]);

static CodeDrop drops[NUM_DROPS];
static pixel_t palette[256];
static uint8_t brightness_buffer[SCREEN_WIDTH * SCREEN_HEIGHT];
static int initialized = 0;

// Reverse a string in place
static void reverse_string(char *str) {
    int len = strlen(str);
    for (int i = 0; i < len / 2; i++) {
        char temp = str[i];
        str[i] = str[len - 1 - i];
        str[len - 1 - i] = temp;
    }
}

// Draw a character at pixel position
static void draw_char(uint8_t *buffer, int x, int y, char c, int brightness) {
    if (c < 32 || c > 126) return;

    const uint8_t *glyph = cp437_font[c - 32];

    // Clear the character area first for crisp rendering
    for (int row = 0; row < CHAR_HEIGHT; row++) {
        for (int col = 0; col < CHAR_WIDTH; col++) {
            int px = x + col;
            int py = y + row;

            if (px >= 0 && px < SCREEN_WIDTH && py >= 0 && py < SCREEN_HEIGHT) {
                int index = py * SCREEN_WIDTH + px;
                // Check if pixel is set in font bitmap (fix horizontal flip)
                if (glyph[row] & (1 << col)) {
                    brightness_buffer[index] = brightness;
                } else {
                    // Clear background pixels for this character
                    if (brightness_buffer[index] > brightness / 3) {
                        brightness_buffer[index] = brightness / 3;
                    }
                }
            }
        }
    }
}

void matrixcode_init(void) {
    // Seed random number generator
    srand(time(NULL));

    // Create green color palette
    for (int i = 0; i < 256; i++) {
        int g = i;
        int r = 0;
        int b = 0;

        // Add some variation for brighter values
        if (i > 240) {
            // Very bright head - almost pure white
            g = 255;
            r = 200 + (i - 240);
            b = 200 + (i - 240);
        } else if (i > 200) {
            // Bright highlights - greenish white
            g = i;
            r = (i - 200) * 4;
            b = (i - 200) * 2;
        } else if (i > 128) {
            // Medium bright - pure green
            g = i;
        } else {
            // Dark green
            g = i;
            r = 0;
        }

        palette[i] = RGB(r, g, b);
    }

    // Initialize all drops as inactive
    for (int i = 0; i < NUM_DROPS; i++) {
        drops[i].active = 0;
        drops[i].x = rand() % (SCREEN_WIDTH / CHAR_WIDTH);
        drops[i].y = -(rand() % 200);  // Start above screen
        drops[i].speed = MIN_SPEED + (rand() % (MAX_SPEED - MIN_SPEED + 1));
        drops[i].length = MIN_LENGTH + (rand() % (MAX_LENGTH - MIN_LENGTH + 1));

        // Pick random code snippet and reverse it
        int snippet_idx = rand() % num_snippets;
        strncpy(drops[i].text, code_snippets[snippet_idx], sizeof(drops[i].text) - 1);
        drops[i].text[sizeof(drops[i].text) - 1] = '\0';
        reverse_string(drops[i].text);
        drops[i].text_len = strlen(drops[i].text);
    }

    // Clear brightness buffer
    memset(brightness_buffer, 0, sizeof(brightness_buffer));

    // Activate some initial drops
    for (int i = 0; i < NUM_DROPS / 3; i++) {
        drops[i].active = 1;
    }

    initialized = 1;
}

void matrixcode_update(pixel_t *pixels, uint32_t time) {
    if (!initialized) {
        matrixcode_init();
    }

    // Fade the screen (creates trail effect)
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) {
        if (brightness_buffer[i] > 0) {
            brightness_buffer[i] = (brightness_buffer[i] * 200) / 256;  // Fade to ~78% (faster fade for clarity)
        }
    }

    // Update and draw each drop
    for (int i = 0; i < NUM_DROPS; i++) {
        if (drops[i].active) {
            // Move drop down
            drops[i].y += drops[i].speed;

            // Draw the drop trail with code characters
            for (int j = 0; j < drops[i].length && j < drops[i].text_len; j++) {
                int y_pos = drops[i].y - (j * CHAR_HEIGHT);
                int x_pos = drops[i].x * CHAR_WIDTH;

                if (y_pos >= -CHAR_HEIGHT && y_pos < SCREEN_HEIGHT) {
                    // Calculate brightness (head is brightest)
                    int brightness;
                    if (j == 0) {
                        // Head - maximum brightness (almost pure white)
                        brightness = 255;
                    } else {
                        // Trail - fade from bright green to dark
                        brightness = 240 - (j * 240 / drops[i].length);
                        if (brightness < 0) brightness = 0;
                    }

                    // Get character from text (cycle through if needed)
                    char c = drops[i].text[j % drops[i].text_len];
                    draw_char(brightness_buffer, x_pos, y_pos, c, brightness);
                }
            }

            // Check if drop is off screen
            if (drops[i].y - (drops[i].length * CHAR_HEIGHT) > SCREEN_HEIGHT) {
                drops[i].active = 0;
            }
        } else {
            // Randomly spawn new drops
            if (rand() % 100 < SPAWN_CHANCE) {
                drops[i].active = 1;
                drops[i].x = rand() % (SCREEN_WIDTH / CHAR_WIDTH);
                drops[i].y = 0;
                drops[i].speed = MIN_SPEED + (rand() % (MAX_SPEED - MIN_SPEED + 1));
                drops[i].length = MIN_LENGTH + (rand() % (MAX_LENGTH - MIN_LENGTH + 1));

                // Pick new random code snippet and reverse it
                int snippet_idx = rand() % num_snippets;
                strncpy(drops[i].text, code_snippets[snippet_idx], sizeof(drops[i].text) - 1);
                drops[i].text[sizeof(drops[i].text) - 1] = '\0';
                reverse_string(drops[i].text);
                drops[i].text_len = strlen(drops[i].text);
            }
        }
    }

    // Render brightness buffer to screen with palette
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) {
        pixels[i] = palette[brightness_buffer[i]];
    }

    // Add some random "glitches" - occasional bright pixels
    if (rand() % 100 < 3) {
        int glitch_x = rand() % SCREEN_WIDTH;
        int glitch_y = rand() % SCREEN_HEIGHT;
        int glitch_index = glitch_y * SCREEN_WIDTH + glitch_x;
        brightness_buffer[glitch_index] = 200 + (rand() % 56);
    }
}

void matrixcode_cleanup(void) {
    // Nothing to clean up (all static memory)
    initialized = 0;
}
