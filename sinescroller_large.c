// Scroll text
static const char *scroll_text = "    WELCOME TO THE LARGE SINE SCROLLER!    THIS IS A TRIBUTE TO THE DEMOSCENE WITH LARGER TEXT AND A BEAUTIFUL STARFIELD...    CODED WITH LOVE IN C AND SDL2    ";

// Scroll position (in pixels)
static int scroll_x = 0;

// Starfield
#define NUM_STARS 512

typedef struct {
    float x, y, z;
} Star;

static Star stars[NUM_STARS];

void sinescroller_large_init(void) {
    // Start scroll position off-screen to the right
    scroll_x = SCREEN_WIDTH;

    // Initialize stars at random positions
    for (int i = 0; i < NUM_STARS; i++) {
        stars[i].x = (float)(rand() % (SCREEN_WIDTH * 2)) - SCREEN_WIDTH;
        stars[i].y = (float)(rand() % (SCREEN_HEIGHT * 2)) - SCREEN_HEIGHT;
        stars[i].z = (float)(rand() % 256) + 1;
    }
}

void sinescroller_large_update(pixel_t *pixels, uint32_t time) {
    // Clear screen to black
    memset(pixels, 0, SCREEN_WIDTH * SCREEN_HEIGHT * sizeof(pixel_t));

    // ===== RENDER STARFIELD =====
    float star_speed = 2.0f;

    for (int i = 0; i < NUM_STARS; i++) {
        // Move star towards viewer
        stars[i].z -= star_speed;

        // Reset star if it passes the viewer
        if (stars[i].z <= 0) {
            stars[i].x = (float)(rand() % (SCREEN_WIDTH * 2)) - SCREEN_WIDTH;
            stars[i].y = (float)(rand() % (SCREEN_HEIGHT * 2)) - SCREEN_HEIGHT;
            stars[i].z = 256.0f;
        }

        // Project 3D position to 2D screen
        float k = 128.0f / stars[i].z;
        int screen_x = (int)(stars[i].x * k + SCREEN_WIDTH / 2);
        int screen_y = (int)(stars[i].y * k + SCREEN_HEIGHT / 2);

        // Check if star is visible on screen
        if (screen_x >= 0 && screen_x < SCREEN_WIDTH &&
            screen_y >= 0 && screen_y < SCREEN_HEIGHT) {

            // Calculate brightness based on depth (closer = brighter)
            int brightness = (int)(255 - stars[i].z);
            if (brightness < 0) brightness = 0;
            if (brightness > 255) brightness = 255;

            // Draw star
            pixel_t color = RGB(brightness, brightness, brightness);
            pixels[screen_y * SCREEN_WIDTH + screen_x] = color;

            // Draw larger stars for closer ones
            if (stars[i].z < 64) {
                // Draw a small cross pattern for bright stars
                if (screen_x > 0) {
                    pixels[screen_y * SCREEN_WIDTH + screen_x - 1] = color;
                }
                if (screen_x < SCREEN_WIDTH - 1) {
                    pixels[screen_y * SCREEN_WIDTH + screen_x + 1] = color;
                }
                if (screen_y > 0) {
                    pixels[(screen_y - 1) * SCREEN_WIDTH + screen_x] = color;
                }
                if (screen_y < SCREEN_HEIGHT - 1) {
                    pixels[(screen_y + 1) * SCREEN_WIDTH + screen_x] = color;
                }
            }
        }
    }

    // ===== RENDER LARGE SINE SCROLLER =====

    // Calculate vertical center position (for 32px tall characters)
    int center_y = SCREEN_HEIGHT / 2 - 16;  // -16 to center the 32px tall font

    // Scroll speed
    scroll_x -= 2;

    // Get text length in pixels (32 pixels per character now, since we're scaling 4x)
    int text_len = strlen(scroll_text) * 32;

    // Wrap around when text goes off screen
    if (scroll_x < -text_len) {
        scroll_x = SCREEN_WIDTH;
    }

    // White color for text
    pixel_t text_color = RGB(255, 255, 255);

    // Draw each character of the scroll text
    for (int i = 0; scroll_text[i] != '\0'; i++) {
        char c = scroll_text[i];

        // Only handle printable ASCII
        if (c < 32 || c > 127) {
            c = 32;  // Replace with space
        }

        int char_x = scroll_x + (i * 32);  // 32 pixels per character (4x scaling)

        // Skip characters that are completely off-screen
        if (char_x < -32 || char_x >= SCREEN_WIDTH) {
            continue;
        }

        // Get font data for this character
        const uint8_t *glyph = cp437_font[c - 32];

        // Draw the character (scaled 4x: 8x8 -> 32x32 pixels) with sine wave skewing
        for (int px = 0; px < 8; px++) {
            // Each font pixel becomes a 4x4 block
            for (int scale_x = 0; scale_x < 4; scale_x++) {
                int screen_x = char_x + (px * 4) + scale_x;
                if (screen_x < 0 || screen_x >= SCREEN_WIDTH) {
                    continue;
                }

                // Calculate sine wave offset for each column to create skew effect
                int wave_phase = (time / 8 + i * 8 + px * 4 + scale_x) & 0xFF;
                int sine_offset = (sine_table[wave_phase] - 128) / 2;  // Range: -64 to +63
                int column_y = center_y + sine_offset;

                // Draw this column of the character
                for (int py = 0; py < 8; py++) {
                    uint8_t row = glyph[py];

                    // Check if pixel is set in font
                    if (row & (1 << px)) {
                        // Each font pixel becomes a 4x4 block
                        for (int scale_y = 0; scale_y < 4; scale_y++) {
                            int screen_y = column_y + (py * 4) + scale_y;
                            if (screen_y >= 0 && screen_y < SCREEN_HEIGHT) {
                                // Overwrite starfield with white text
                                pixels[screen_y * SCREEN_WIDTH + screen_x] = text_color;
                            }
                        }
                    }
                }
            }
        }
    }
}

