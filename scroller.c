// Scroll text
static const char *scroll_text = "    WELCOME TO THE CLASSIC SINE SCROLLER!    THIS IS A TRIBUTE TO THE DEMOSCENE OF THE 80S AND 90S...    CODED WITH LOVE IN C AND SDL2    ";

// Scroll position (in pixels)
static int scroll_x = 0;

// Palette for text (gradient from dark to bright)
static pixel_t text_palette[16];

void scroller_init(void) {
    // Create a vibrant rainbow palette (red->yellow->green->cyan->blue->magenta->red)
    for (int i = 0; i < 16; i++) {
        int r, g, b;
        float hue = (i * 360.0f) / 16.0f;  // 0-360 degrees

        // Convert HSV to RGB (S=1, V=1 for vibrant colors)
        int h_sector = (int)(hue / 60.0f) % 6;
        float f = (hue / 60.0f) - h_sector;
        int p = 0;
        int q = (int)(255 * (1 - f));
        int t = (int)(255 * f);

        switch (h_sector) {
            case 0: r = 255; g = t;   b = p;   break;  // Red to Yellow
            case 1: r = q;   g = 255; b = p;   break;  // Yellow to Green
            case 2: r = p;   g = 255; b = t;   break;  // Green to Cyan
            case 3: r = p;   g = q;   b = 255; break;  // Cyan to Blue
            case 4: r = t;   g = p;   b = 255; break;  // Blue to Magenta
            case 5: r = 255; g = p;   b = q;   break;  // Magenta to Red
            default: r = 255; g = 0; b = 0; break;
        }

        text_palette[i] = RGB(r, g, b);
    }

    // Start scroll position off-screen to the right
    scroll_x = SCREEN_WIDTH;
}

void scroller_update(pixel_t *pixels, uint32_t time) {
    // Clear screen to black
    memset(pixels, 0, SCREEN_WIDTH * SCREEN_HEIGHT * sizeof(pixel_t));

    // Calculate vertical center position
    int center_y = SCREEN_HEIGHT / 2 - 4;  // -4 to center the 8px tall font

    // Scroll speed
    scroll_x -= 2;

    // Get text length in pixels
    int text_len = strlen(scroll_text) * 8;

    // Wrap around when text goes off screen
    if (scroll_x < -text_len) {
        scroll_x = SCREEN_WIDTH;
    }

    // Draw each character of the scroll text
    for (int i = 0; scroll_text[i] != '\0'; i++) {
        char c = scroll_text[i];

        // Only handle printable ASCII
        if (c < 32 || c > 127) {
            c = 32;  // Replace with space
        }

        int char_x = scroll_x + (i * 8);

        // Skip characters that are completely off-screen
        if (char_x < -8 || char_x >= SCREEN_WIDTH) {
            continue;
        }

        // Get font data for this character
        const uint8_t *glyph = cp437_font[c - 32];

        // Draw the character (8x8 pixels) with sine wave skewing
        for (int px = 0; px < 8; px++) {
            int screen_x = char_x + px;
            if (screen_x < 0 || screen_x >= SCREEN_WIDTH) {
                continue;
            }

            // Calculate sine wave offset for each column to create skew effect
            int wave_phase = (time / 8 + i * 8 + px) & 0xFF;
            int sine_offset = (sine_table[wave_phase] - 128) / 4;  // Range: -32 to +31
            int column_y = center_y + sine_offset;

            // Draw this column of the character
            for (int py = 0; py < 8; py++) {
                int screen_y = column_y + py;
                if (screen_y < 0 || screen_y >= SCREEN_HEIGHT) {
                    continue;
                }

                uint8_t row = glyph[py];

                // Check if pixel is set in font
                if (row & (1 << px)) {
                    // Add some color variation based on vertical position
                    int color_idx = (py + sine_offset / 4 + 4) & 0x0F;
                    pixels[screen_y * SCREEN_WIDTH + screen_x] = text_palette[color_idx];
                }
            }
        }
    }
}

